  <h4>Rules I Use</h4>
  <p>
    Grapples and shove attacks can replace any one attack, including opportunity attacks, and this applies to monsters as well. Meaning monsters with multiattack can replace any attack they have with a grapple/shove assuming they have the appendages to do so.
  </p>
  <p>
    Injuries. Being reduced to 0 hit points requires a roll on the injury table. See the quick reference guide for details.
  </p>
  <p>
    Class modifications! In particular, wizards, sorcerers, rangers, and warlocks get additional spells known based on subclass, similar to a cleric's domain spells. For sorcerers and rangers this only applies to the subclasses that did not get this type of feature already. There are also a number of homebrewed subclasses and a homebrewed full class (factotum) available on this site. Eldritch Knights can use the custom Eldritch Knight spell list instead of the wizard spell list (which includes a number of useful 'bonus action' cast spells from the Paladin and Ranger lists).
  </p>
  <p>
    Languages known are based on where you grew up, not race. Additional languages known can be had with high intelligence &amp; charisma (see languages page on this site).
  </p>
  <p>
    There is no Common.
  </p>
  <p>
    3rd edition diagonals (first diagonal costs 5 ft, second diagonal costs 10).
  </p>
  <h4>Rules I Would Like to Try</h4>
  <p>
    Hidden death saves. Players do not know the results of each others death saves.
  </p>
  <p>
    Extended rests. Instead of increasing the amount of time short and long rests take, this alternate rule would simply alter the amount of time it takes for HP recovery. Specifically, while long rests still allow you to recover hit dice as normal, you do not automatically get all of your hit points back. Instead you recover half your hit dice at the end of a long rest and can spend them at the end. To recover all hit points would require an extended rest, a full day of recovery in which no strenuous activity is attempted.
  </p>
  <p>
    No tie-breakers on initiative ties, instead monsters always lose ties.
  </p>
  <p>
    Intelligence points: This would preempt the bonus language rule above. Players get a number of points at character creation equal to their intelligence mod that they can spend on additional proficiencies. 1 point for a tool or language proficiency, 2 points for a skill proficiency, or 3 points for expertise in a skill.
  </p>
  <p>
    Crunchy Crits: Critical hits do maximum damage + rerolled dice.
  </p>
  <p>
    Modified weapons rules to increase diversity by adding additional reach lengths, potential weapon effects and limitations, new weapons, etc
  </p>
</div>